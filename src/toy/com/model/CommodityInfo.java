package toy.com.model;

public class CommodityInfo {
	int commID;
	String commName;
	int commPrice;
	int commFunction;
	int commEffi;
	String commFile;
	/**
	 * @return the commID
	 */
	public int getCommID() {
		return commID;
	}
	/**
	 * @param commID the commID to set
	 */
	public void setCommID(int commID) {
		this.commID = commID;
	}
	/**
	 * @return the commName
	 */
	public String getCommName() {
		return commName;
	}
	/**
	 * @param commName the commName to set
	 */
	public void setCommName(String commName) {
		this.commName = commName;
	}
	/**
	 * @return the commPrice
	 */
	public int getCommPrice() {
		return commPrice;
	}
	/**
	 * @param commPrice the commPrice to set
	 */
	public void setCommPrice(int commPrice) {
		this.commPrice = commPrice;
	}
	/**
	 * @return the commFunction
	 */
	public int getCommFunction() {
		return commFunction;
	}
	/**
	 * @param commFunction the commFunction to set
	 */
	public void setCommFunction(int commFunction) {
		this.commFunction = commFunction;
	}
	/**
	 * @return the commEffi
	 */
	public int getCommEffi() {
		return commEffi;
	}
	/**
	 * @param commEffi the commEffi to set
	 */
	public void setCommEffi(int commEffi) {
		this.commEffi = commEffi;
	}
	/**
	 * @return the commFile
	 */
	public String getCommFile() {
		return commFile;
	}
	/**
	 * @param commFile the commFile to set
	 */
	public void setCommFile(String commFile) {
		this.commFile = commFile;
	}
	
	

}
